// Build category groups
export const CATEGORY_GROUPS: CategoryGroup[] = Object.entries(RAW_CATEGORIES).map(([primary, detailedList]) => ({
    primary,
    primaryDisplay: sanitizePrimary(primary),
    options: detailedList.map(detailed => ({
        primary,
        detailed,
        primaryDisplay: sanitizePrimary(primary),
        detailedDisplay: sanitizeDetailed(detailed, primary),
    })),
}));

// Flat list for dropdowns
export const ALL_CATEGORIES: CategoryOption[] = CATEGORY_GROUPS.flatMap(g => g.options);

// Lookup map: raw detailed → display names
const DISPLAY_MAP: Record<string, { primary: string; detailed: string }> = {};
ALL_CATEGORIES.forEach(c => {
    DISPLAY_MAP[c.detailed] = { primary: c.primaryDisplay, detailed: c.detailedDisplay };
});

// Primary-only lookup
const PRIMARY_DISPLAY_MAP: Record<string, string> = {};
CATEGORY_GROUPS.forEach(g => {
    PRIMARY_DISPLAY_MAP[g.primary] = g.primaryDisplay;
});

/**
 * Get sanitized display names for category
 */
export function getCategoryDisplay(primaryRaw: string | null, detailedRaw: string | null): { primary: string; detailed: string } {
    if (detailedRaw && DISPLAY_MAP[detailedRaw]) {
        return DISPLAY_MAP[detailedRaw];
    }
    if (primaryRaw) {
        return {
            primary: PRIMARY_DISPLAY_MAP[primaryRaw] || sanitizePrimary(primaryRaw),
            detailed: '',
        };
    }
    return { primary: 'Uncategorized', detailed: '' };
}