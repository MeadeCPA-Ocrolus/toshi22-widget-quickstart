/**
 * Plaid Personal Finance Categories (PFCv2)
 * 
 * Complete taxonomy with sanitized display names for CPA readability.
 * 
 * @module constants/plaidCategories
 */

export interface CategoryOption {
    primary: string;        // Raw: FOOD_AND_DRINK
    detailed: string;       // Raw: FOOD_AND_DRINK_RESTAURANT
    primaryDisplay: string; // Display: Food & Drink
    detailedDisplay: string;// Display: Restaurant
}

export interface CategoryGroup {
    primary: string;
    primaryDisplay: string;
    options: CategoryOption[];
}

// Sanitize: FOOD_AND_DRINK → Food & Drink
function sanitizePrimary(s: string): string {
    return s.split('_').map(w => w === 'AND' ? '&' : w === 'NON' ? 'Non' : w.charAt(0) + w.slice(1).toLowerCase()).join(' ');
}

// Sanitize detailed: FOOD_AND_DRINK_RESTAURANT → Restaurant
function sanitizeDetailed(detailed: string, primary: string): string {
    const suffix = detailed.replace(`${primary}_`, '');
    return suffix.split('_').map(w => w === 'AND' ? '&' : w.charAt(0) + w.slice(1).toLowerCase()).join(' ');
}

